package model;

public class ModelNumber {

    private int number;
    private int powNumber;

    public ModelNumber(int number, int powNumber) {
        this.number = number;
        this.powNumber = powNumber;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getPowNumber() {
        return powNumber;
    }

    public void setPowNumber(int powNumber) {
        this.powNumber = powNumber;
    }


}
